# Chain-of-Thought reasoning without API

def solve_problem():
    print("Solving step by step...\n")

    # Step 1
    speed = 60
    print("Step 1: Train speed =", speed, "km per hour")

    # Step 2
    time = 3.5
    print("Step 2: Time =", time, "hours")

    # Step 3
    distance = speed * time
    print("Step 3: Distance = Speed × Time")

    # Step 4
    print("Step 4: Distance =", speed, "×", time, "=", distance)

    print("\nFinal Answer: The train will travel", distance, "km.")

# Run the function
solve_problem()